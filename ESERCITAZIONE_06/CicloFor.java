class CicloFor {
	public static void main(String[] args){
		for(int i = 1; i <= 7; i++){
			System.out.println(i);
		}
	}
}

//Sicuramente il ciclo for è il più semplice ed intuitivo da capire, non solo per la sua compattezza, ma anche e sopratutto per riuscire a capire 
//come viene eseguito il ciclo in una sola riga. Nella prima riga di inizializzazione del ciclo troviamo tutte le informazioni necessarie per farci
