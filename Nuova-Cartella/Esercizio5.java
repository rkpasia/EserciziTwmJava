/*Programma che calcola più volte il perimetro e l'area di un quadrato modificando più volte il lato di esso*/
class Esercizio5 {
  	public static void main (String[] args) {
    	int lato = 12;
		System.out.println("Il lato del quadrato è: "+lato+"\nPerimetro del quadrato: "+ 4*lato +"\nArea del quadrato: "+ lato*lato+"\n");
		lato = 32;
		System.out.println("Il lato del quadrato è: "+lato+"\nPerimetro del quadrato: "+ 4*lato +"\nArea del quadrato: "+ lato*lato+"\n");
		lato = 333;
		System.out.println("Il lato del quadrato è: "+lato+"\nPerimetro del quadrato: "+ 4*lato +"\nArea del quadrato: "+ lato*lato+"\n");
  	}
}